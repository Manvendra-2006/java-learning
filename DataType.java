class DataType{
    public static void main(String args[]){
        int a = 9;
        boolean b = true;
        char c = 'g';
        float g = 8.9f; // Here we have to write f after number because compiler think it is a double
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(g);
    }
}